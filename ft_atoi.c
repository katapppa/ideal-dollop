#include "libft.h"

int		ft_atoi(const char *str)
{
	int i;
	int n;
	int b;

	i = 0;
	n = 0;
	b = 0;
	while (str[i] == '\t' || str[i] == '\v' || str[i] == '\f' || str[i]
			== '\r' || str[i] == ' ' || str[i] == '\0' || str[i] == '\n')
		i++;
	if (str[i] == '-')
		b = 1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] != '\0' && str[i] >= '0' && str[i] <= '9')
	{
		n = 10 * n + (str[i] - '0');
		i++;
	}
	if (b == 1)
		return (n * (-1));
	return (n);
}
